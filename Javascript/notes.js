// Execution Context 
// Defination : Execution context is an abstract concept of an environment where the Javascript code is evaluated and executed.
// Things that happen in execution context:
// 1. Creation of Global Object
// 2. Creation of this keyword
// 3. Creation of Outer Environment
// 4. Creation of variables, functions and arguments

//  Execution Context Stack 
//  Defination : Execution context stack is a stack of execution contexts. The topmost execution context is the one that is currently running.

//Lexical Environment
// Defination : Lexical environment is a data structure that holds identifier-variable mapping. It is used to determine how an expression is evaluated to reference a variable.

// Hoisting
// Defination : Hoisting is a JavaScript mechanism where variables and function declarations are moved to the top of their scope before code execution.

// Scope
// Defination : Scope is the accessibility of variables, functions, and objects in some particular part of your code during runtime. In other words, scope determines the visibility of variables and other resources in areas of your code.

// Closure
// Defination : Closure is a feature in JavaScript where an inner function has access to the outer (enclosing) function’s variables — a scope chain. The closure has three scope chains: it has access to its own scope (variables defined between its curly brackets), it has access to the outer function’s variables, and it has access to the global variables.

// Callback
// Defination : A callback function is a function passed into another function as an argument, which is then invoked inside the outer function to complete some kind of routine or action.

// Asynchronous
// Defination : Asynchronous refers to the occurrence of events independent of the main program flow and ways to deal with such events.

// Event Loop
// Defination : The event loop is a programming construct or design pattern that waits for and dispatches events or messages in a program.

// Promise
// Defination : A promise is an object that may produce a single value some time in the future: either a resolved value, or a reason that it’s not resolved (e.g., a network error occurred). A promise may be in one of 3 possible states: fulfilled, rejected, or pending.

// Async/Await
// Defination : Async/await is a new way to write asynchronous code. Previous options for asynchronous code are callbacks and promises.

// Call Stack
// Defination : The call stack is a data structure that uses the Last In, First Out (LIFO) principle to temporarily store and manage function invocation (call).

// Memory Heap
// Defination : Memory Heap is the place in the memory where the memory allocation takes place. It is the heap part of the memory where the objects are stored.

// Single Threaded
// Defination : Single threaded means that only one set of instructions is executed at a time.

// Thread of Execution
// Defination : Thread of execution is the smallest sequence of programmed instructions that can be managed independently by a scheduler, which is typically a part of the operating system.

// Node.js
// Defination : Node.js is an open-source, cross-platform, JavaScript runtime environment that executes JavaScript code outside a web browser.

// Spread Operator
// Defination : Spread operator allows an iterable to expand in places where 0+ arguments are expected. It is mostly used in the variable array where there is more than 1 values are expected.

// Rest Operator
// Defination : Rest operator is used to merge multiple values into an array. It is mostly used in the parameter of a function where there is more than 1 values are expected.

// var vs let vs const
// Defination : var, let and const are all used to declare variables in JavaScript. var is the oldest keyword whereas let and const are introduced in ES6. var and let are pretty similar but have some differences. const is used to declare constants.

// == vs ===
// Defination : == and === are comparison operators in JavaScript. == checks for equality of value whereas === is a strict equality operator which checks for both value and datatype.

// First Class Functions
// Defination : First class functions means that in JavaScript, functions are treated as first-class citizens. This means that they can be treated like any other variable, and can be passed as an argument of another function, can be returned by another function and can be assigned as a value to a variable.

// Higher Order Functions
// Defination : Higher order functions are functions that operate on other functions, either by taking them as arguments or by returning them. In simple words, A Higher-Order function is a function that receives a function as an argument or returns the function as output.

// Truty and Falsy Values
// Defination : In JavaScript, a falsy value is a value that is considered false when encountered in a Boolean context. JavaScript uses type conversion to coerce any value to a Boolean in contexts that require it, such as conditionals and loops.

