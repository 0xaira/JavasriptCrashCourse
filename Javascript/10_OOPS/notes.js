//Defination: An object is a collection of properties, and a property is an association between a name (or key) and a value. A property's value can be a function, in which case the property is known as a method. In addition to objects that are predefined in the browser, you can define your own objects.

//Objects in Javascript are mutable keyed collections. They are written as name:value pairs, separated by commas, enclosed in curly braces. The name is always a string, the value can be any valid Javascript value.

